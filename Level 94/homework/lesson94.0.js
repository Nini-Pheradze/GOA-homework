// Use concat() to join three strings: "Good", " ", "Morning".

const str2 = "Good";
const str3 = " ";
const str4 = "Morning";
const result1 = str2.concat(str3, str4);

console.log(result1);

// Concatenate a string and a number (converted to string) using concat().

const str5 = "Nini";
const int0 = 1;
const result2 = str5.concat(int0);

console.log(result2);

// Join a string with a special character, like "!" or "?", using concat().

const str6 = "Morning";
const str7 = "!";
