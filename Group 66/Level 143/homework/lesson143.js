// აბრუნებს ყველა დოკუმენტს პოსტების კოლექციიდან
db.posts.find();

// აბრუნებს ყველა დოკუმენტს უფრო წაკითხვად (JSON) ფორმატში
db.posts.find().pretty();

// აბრუნებს მხოლოდ ერთ კონკრეტულ (პირველს) დოკუმენტს, რომელიც აკმაყოფილებს პირობას
db.posts.findOne({ title: "My Post" });

// ძებნა კონკრეტული კუთვნილების მიხედვით (მაგ: სადაც ავტორი არის "Gigi")
db.posts.find({ authorName: "Gigi" });

// ძებნა ჩაშენებული (Nested) ობიექტის მიხედვით (Dot Notation - აუცილებელია ბრჭყალები)
db.posts.find({ "metadata.category": "Technology" });

// ==========================================================
// 3. სორტირება და ლიმიტი (Sort & Limit)
// ==========================================================

// სორტირება: 1 ნიშნავს ზრდადობას, -1 ნიშნავს კლებადობას
// მაგალითი: პოსტების დალაგება likesCount-ის მიხედვით კლებადობით (ყველაზე პოპულარულები პირველად)
db.posts.find().sort({ likesCount: -1 });

// ლიმიტი: აბრუნებს მხოლოდ მითითებული რაოდენობის დოკუმენტს (მაგ: მხოლოდ პირველი 5)
db.posts.find().limit(5);

// კომბინაცია: ყველაზე პოპულარული 3 პოსტის ნახვა
db.posts.find().sort({ likesCount: -1 }).limit(3);

// ==========================================================
// 4. შედარების ოპერატორები (Comparison Operators)
// ==========================================================

/* $gt  (Greater Than)       -> მეტია
$lt  (Less Than)          -> ნაკლებია
$gte (Greater Than/Equal) -> მეტია ან ტოლია
$lte (Less Than/Equal)    -> ნაკლებია ან ტოლია
*/

// $gt: პოსტები, რომელთაც აქვთ 100-ზე მეტი ლაიქი
db.posts.find({ likesCount: { $gt: 100 } });

// $lt: პოსტები, რომელთა ნახვები (views) 50-ზე ნაკლებია
db.posts.find({ views: { $lt: 50 } });

// $gte: პოსტები, სადაც კომენტარების რაოდენობა არის 10 ან მეტი
db.posts.find({ commentsCount: { $gte: 10 } });

// $lte: პოსტები, სადაც რეიტინგი არის 4.5-ზე ნაკლები ან ტოლი
db.posts.find({ rating: { $lte: 4.5 } });

// დიაპაზონი: პოსტები, სადაც ლაიქები არის 50-დან 150-მდე
db.posts.find({ likesCount: { $gte: 50, $lte: 150 } });

// ==========================================================
// 5. სხვა სასარგებლო ბრძანებები
// ==========================================================

// ითვლის რამდენი პოსტია კოლექციაში
db.posts.countDocuments({});

// ითვლის რამდენი პოსტია კონკრეტული კატეგორიიდან (მაგ: "Art")
db.posts.countDocuments({ category: "Art" });