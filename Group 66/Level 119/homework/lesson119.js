// 3. TLS (Transport Layer Security)
// TLS არ არის დამოუკიდებელი გადაცემის პროტოკოლი (როგორც TCP ან UDP), ის არის უსაფრთხოების შრე, რომელიც ემატება სხვა პროტოკოლებს (ძირითადად TCP-ს).

