// როგორ მუშაობს: TLS იყენებს სერთიფიკატებსა და კრიპტოგრაფიულ გასაღებებს. სწორედ TLS-ის დამსახურებაა, რომ http:// ხდება https:// (სადაც "S" ნიშნავს Secure).

