// 1. მასივის ზუსტი შედარება (Exact Match)
// მოძებნის მხოლოდ იმ პოსტს, რომლის თეგები არის ზუსტად ["films", "drama"].
// მნიშვნელოვანია: თანმიმდევრობა უნდა იყოს ზუსტი და სხვა თეგი არ უნდა ეწეროს.
db.posts.find({ tags: ["films", "drama"] });


// 2. ერთი ელემენტის შემოწმება (Single Element)
// მოძებნის ყველა პოსტს, რომლის თეგების მასივი შეიცავს "drama"-ს მაინც.
// არ აქვს მნიშვნელობა, სხვა რა თეგებია მასივში.
db.posts.find({ tags: "drama" });


// 3. რამდენიმე ელემენტის შემოწმება ($all)
// მოძებნის პოსტებს, სადაც მასივში არის "films"-იც და "drama"-ც.
// თანმიმდევრობას მნიშვნელობა არ აქვს.
db.posts.find({ tags: { $all: ["films", "drama"] } });


// 4. რამდენიმედან ერთ-ერთის შემოწმება ($in)
// მოძებნის პოსტებს, სადაც თეგებში არის ან "horror" ან "comedy".
db.posts.find({ tags: { $in: ["horror", "comedy"] } });


// 5. მასივის ზომის მიხედვით ძებნა ($size)
// მოძებნის პოსტებს, რომელთაც აქვთ ზუსტად 3 თეგი.
db.posts.find({ tags: { $size: 3 } });


// 6. შედარების ოპერატორები მასივის ელემენტებზე
// მოძებნის პოსტებს, სადაც "years" მასივის რომელიმე ელემენტი მეტია 2020-ზე.
db.posts.find({ years: { $gt: 2020 } });


// 7. $elemMatch - რთული ძებნა მასივში
// გამოიყენება მაშინ, როცა მასივში ობიექტებია ან გვინდა, რომ ერთი და იგივე 
// ელემენტი აკმაყოფილებდეს რამდენიმე პირობას ერთდროულად.
// მაგალითად: მოძებნე პოსტი, რომლის წლების მასივში არის წელი, რომელიც 
// ერთდროულად მეტია 2010-ზე და ნაკლებია 2015-ზე.
db.posts.find({ years: { $elemMatch: { $gt: 2010, $lt: 2015 } } });